# In Ruby, there are different *types* of data:

# Numbers
puts 3
puts 2
puts 3 + 2
puts 3 - 2
puts 3 * 2 
puts 3 / 2.0
puts 7 / 4

# Perform simple math with numbers
puts 5 * 2 - 2
puts 5 * (2 - 2)

# Strings
puts "Hello, world"

# Combine strings together
puts "Hello, " + "world"

puts "Tacos" * 3

# Variables
food = "Tacos"
number = 3

puts food
puts number

puts (food + " ") * number

# Combine strings and variables
greeting = "Hello sir Jim"
welcome_message = "#{greeting}"

# String manipulation
puts welcome_message.reverse 